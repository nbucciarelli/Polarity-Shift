#pragma once

typedef unsigned int EvID;

class gameEvent
{
protected:
	EvID id;
	void* data;
public:
	gameEvent(EvID eventID, void* dat = 0)
	{
		id = eventID;
		data  = dat;
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////
	//	Function:		"getEventID"
	//	Last Modified:	August 25, 2008
	//	Purpose:		returns the event ID
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	EvID getEventID(void) { return id; }

	//////////////////////////////////////////////////////////////////////////////////////////////////////
	//	Function:		"getData"
	//	Last Modified:	August 25, 2008
	//	Purpose:		returns data
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	void* getData(void) { return data; }
};