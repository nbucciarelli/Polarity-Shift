#pragma once

typedef unsigned int mID;

class message
{
	mID id;
protected:
public:

	message(mID msg = 0) { id = msg; }

	virtual ~message(void) {}

	//////////////////////////////////////////////////////////////////////////////////////////////////////
	//	Function:		"getID"
	//	Last Modified:	August 25, 2008
	//	Purpose:		returns the ID of the message
	//////////////////////////////////////////////////////////////////////////////////////////////////////
	mID getID(void) { return id; }
};